				<p>When it comes to consuming coffee, our environmental and sustainable mindfulness needn’t stop at using reusable pods. In fact there are further ways we can reuse our coffee after making and enjoying that cup that can go along way to ensuring that coffee truly becomes a zero emissions product.</p>
