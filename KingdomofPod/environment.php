				<p>The disposable coffee pod is having a detrimental impact on the environment.</p>
				<p>Coffee pod manufacturers and sellers claim that their coffee pods are recyclable. But there is a big difference between if something is recyclable and if it its cost effective to recycle. With most recycling plants refusing coffee pods they are instead sent to landfill sites. A single disposable aluminum or plastic pod can take anywhere from 150 to 500 years or more to degrade. In Australia we are consuming 3 million of these a day!</p>
				<p>By using reusable coffee pods, we can go some way to ensuring that all the time and effort spent on securing and improving our growing techniques isn’t thrown away once we have consumed that delicious cup of coffee. </p>
